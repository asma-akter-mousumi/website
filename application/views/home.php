                                <p style="padding:20px;color: white;">Kite Bangladesh Limited, primarily started in November, 2010 with the motto boosting Quality Education for All by integrating different Japanese teaching-learning methods in Bangladesh contexts, and worked in the spectrum of Early Childhood Development, counselling & parenting, language courses, e-learning materials, toys and pictorial story books. 
The organization has been recently restructured to concentrate majorly in developing the areas of formal and non-formal education of the country. Kite has been implementing a number of educational projects. Computer Literacy Program is the pioneer among all sort of projects, an award winning non-formal model, which is incessantly running to make underprivileged youths in rural Bangladesh- digital literate.  Another project named Smart Classroom supported a paradigm shift from traditional “teaching by telling” to “distributed learning” that ultimately enables “learning by doing.”  It uses multimedia content CD and Internet, where available, a laptop and a widescreen monitor in the classroom to simplify the teaching-learning. Upgradation of the contents is continuous process, and recently the number of content reaches to 1100. Connecting Parents is a project through which schools get access to a web based platform where school can update and maintain database of students’ all sorts of performances, and parents would be able to get notified over texting directly from the web-based platform. 


