                        <p style="font-size: 24px;padding: 30px;color:#807d7d">
                           The School Communication Platform (SCP)   is a web based platform that a school hosts to connect with all stakeholders including students, parents, school management committee and the administration for taking advantage of the modern ICT opportunities. This system aims to achieve: 
•	Improve communication among school stakeholders;
•	Educators retrieve data according to their need;
•	Parents  take necessary attempts to improve his/her child’s education;
•	Teachers and guardian optimize interaction  time & labor 
•	Track the academic progress by stakeholders.
 This tool allows parents to monitor child’s regular activities including school programs, exam results, class attendance, and gain access to admission forms. The system allows direct connection with parents via SMS (Short Message Service) text messages via the mobile phone. 



